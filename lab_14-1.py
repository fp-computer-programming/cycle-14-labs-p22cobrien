import turtle

window = turtle.Screen()
turtl = turtle.Turtle()

window.setup(1000,1000)
window.screensize(500,500)
window.title("Lab 1")

turtl.forward(250)
turtl.right(90)
turtl.forward(100)
turtl.right(90)
turtl.forward(250)
turtl.right(90)
turtl.forward(100)

window.mainloop()
